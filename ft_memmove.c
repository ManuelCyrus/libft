#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	unsigned char	*tmp1;
	unsigned char	*tmp2;

	if (!n)
		return (dest);
	tmp1 = (unsigned char *)dest;
	tmp2 = (unsigned char *)src;
	if (dest < src)
		return (ft_memcpy(dest, src, n));
	if (dest > src)
	{
		while (n--)
			tmp1[n] = tmp2[n];
	}
	return (dest);
}
